
class Persona{
    // agregar constructor 
    constructor(nombre, apellido){
        // this.argumento = parametro 
        this.nombre = nombre;
        this.apellido = apellido;
    }
}
 
// crear objeto

let persona1 = new Persona("juan", "perez");
console.log(persona1);

let persona2 = new Persona("carlos", "lara");
console.log(persona2);

